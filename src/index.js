const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};


function decode(expr) {
    const space = new Array(10).fill('*').join('');
    return expr
    .match(/.{1,10}/g)
    .map(it => it.slice('') === space ? ' ' : it)
    .map(it => it.match(/.{1,2}/g))
    .map(it => 
         it.map(item => (item === '10') ? '.' : (item === '11') ? '-' : item === ' ' ? ' ' : '')
    )
    .map(it => it.join(''))
    .map(it => (it !== ' ') ? MORSE_TABLE[it] : it)
    .join('')
 }

module.exports = {
    decode
}